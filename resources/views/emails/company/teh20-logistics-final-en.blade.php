## Free Parking PROCEDURE 

In the standard offering (without buying additional one) you have a **one parking card** (will be available in the "welcome pack")

When entering the parking from the street FOR THE FIRST TIME you take the receipt as a normal guest. For leaving and consecutive entering/leaving (like for next day) you MUST use parking card provided by us (otherwise the fee will apply).

## (Optional) Setup day - 19th of October

Assembly hours 4pm-8pm (if not )

After entering the main entrance you need to go to the **information point for the exhibitors** (located in the  Hall 1) . Your Account Manager will happily await :)

In case of any problem (e.g., lack of furniture, electricity) you should go to the information for exhibitors or call your Account Manager, whose number you will find in the welcome pack. 

## Event Day - 20th of October

In the standard offering (without buying additional one) you have a **one parking card**, which is only needed to leave the parking area. When entering the parking from the street you always take the receipt as normal guest - you need our voucher to leave without a fee)

Additional setup hours are between **8:00am and 9:30pm.** If you have your booth built by us this is sufficient moment to show at the Expo.

After entering the hall you need to go to the information point for the exhibitors (located in the VIP Zone near the entrance to the expo hall) . Your Account Manager (or team member in charge) should be there.

At 10:00am we will open gates and welcome visitors.

In case of any problem (e.g., with electricity) you should go to the information for exhibitors or text your Account Manager, whose number you will find in the welcome pack.

**Disassembly** is possible from 5:15pm till 8:00pm.

## Welcome pack

During our first meeting (optional setup day or immediately before the event) you will be handed a welcome pack with: 

* the badges of your representatives if you have added them before the deadline at your exhibitor's panel, 
* lunch vouchers (number of vouchers = number of representatives registered but max 4... need more? please buy addditional via Additional services in your Exhibitor's Panel)
* contact numbers to the important staff of Expo, 
* parking card
* any additionally paid vouchers, parking cards...

## Additional recommendations

** Please take your own power extension cord / bar ** (of course we have some backup but...)

** Remember, that standard power access is only 300 Watts ** 

If you plan to use something more that, for instance, 2 laptops and TV you need to buy an extra package (3000 Watts).  If you want to consult power usage of your equipment, please contact with us.


